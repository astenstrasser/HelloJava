
public class TestingForLoops {

	public static void main(String[] args) {

// 	for syntax: for (declaring variable, executed only once ; 
//		              boolean condition to know if the loop should be executed ; 
//		              what should be executed at the end of every repetition)

		for (int tracker = 0; tracker <= 10; tracker++) {
			System.out.println(tracker);
		}

//	it is not possible to print the variable tracker outside the for statement.
	}
}