
public class TestingDouble {
	
	public static void main(String[] args) {
		
		double salary = 1250.70;
		System.out.println("The salary is "+salary);
		
		double division = 3.14/2;
		System.out.println("The result is "+division);
		
		int divisionInteger = 5/2;
		System.out.println("The integer result is "+divisionInteger+" , but it should be 2.5");
		
		double divisionDouble = 5/2;
		System.out.println("Now the double result is "+divisionDouble);
		
		double correctDivision = 5.0/2;
		System.out.println("Correct result "+correctDivision);
		
	}

}
